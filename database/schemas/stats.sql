CREATE TABLE player_stats (
    stat_id SERIAL PRIMARY KEY,
    game_id INTEGER NOT NULL,
    player_id INTEGER NOT NULL,
    pts INTEGER DEFAULT 0,
    rebs INTEGER DEFAULT 0,
    asts INTEGER DEFAULT 0,
    stls INTEGER DEFAULT 0,
    blks INTEGER DEFAULT 0,
    turnovers INTEGER DEFAULT 0,
    fouls INTEGER DEFAULT 0,
    fg2m INTEGER DEFAULT 0,
    fg2a INTEGER DEFAULT 0,
    fg3m INTEGER DEFAULT 0,
    fg3a INTEGER DEFAULT 0,
    fgm INTEGER DEFAULT 0,
    fga INTEGER DEFAULT 0,
    ftm INTEGER DEFAULT 0,
    fta INTEGER DEFAULT 0,
    dnp BOOLEAN DEFAULT FALSE,
    fp DECIMAL(5,2) DEFAULT 0.0,
    FOREIGN KEY (game_id) REFERENCES games(game_id),
    FOREIGN KEY (player_id) REFERENCES players(player_id)
);